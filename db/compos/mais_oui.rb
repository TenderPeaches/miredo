"une dernière étreinte
un calme après le verre d'eau?
pourtant s'envolent
ces rêves qui m'ont habité
aujourd'hui creux, émacié
où sont ces couleurs que je ne sache dessiner?
parle moi d'un vert qui n'est pas rouillé
ou de ces bleus qui n'ont jamais connu le gris

ce n'est rien, dis-tu
et depuis quand
avons-nous arrêtés
d'avoir peur du néant?
celui qui s'engouffre
par ces paroles bien tissées
dehors, nous y voilà
allons-nous enfin y mettre le nez?"